public class VeiculoFluvial extends Veiculo {

    private String registroMarinha;
    private double boca;
    private double caladoAereo;

    public String getRegistroMarinha() {
        return registroMarinha;
    }

    public void setRegistroMarinha(String registroMarinha) {
        this.registroMarinha = registroMarinha;
    }

    public double getBoca() {
        return boca;
    }

    public void setBoca(double boca) {
        this.boca = boca;
    }

    public double getCaladoAereo() {
        return caladoAereo;
    }

    public void setCaladoAereo(double caladoAereo) {
        this.caladoAereo = caladoAereo;
    }
}